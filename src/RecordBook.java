public class RecordBook {
    char pickUpPoint;
    int pickUpTime;
    char dropPoint;
    int earn;

    public RecordBook(char pickUpPoint, int pickUpTime, char dropPoint, int earn) {
        this.pickUpPoint = pickUpPoint;
        this.pickUpTime = pickUpTime;
        this.dropPoint = dropPoint;
        this.earn = earn;
    }
}

